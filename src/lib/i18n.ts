/**
 * Obtiene una traducción para la clave dada en el idioma especificado
 * @param key Clave de traducción en formato 'categoria.subcategoria.texto'
 * @param lang Código de idioma ('es', 'en')
 * @returns La traducción correspondiente o la clave si no se encuentra
 */
export function t(key: string, lang: SupportedLanguage = 'es'): string {
  const keys = key.split('.');
  let result: any = translations[lang] || translations.es; // Fallback a español si el idioma no existe
  
  // Navegar por el árbol de traducciones según las claves
  for (const k of keys) {
    if (result && typeof result === 'object' && k in result) {
      result = result[k];
    } else {
      // Si no se encuentra la clave, devolver la clave original
      console.warn(`[i18n] Clave de traducción no encontrada: ${key} (idioma: ${lang})`);
      return key;
    }
  }
  
  return result as string;
}

/**
 * Hook personalizado para obtener el idioma del usuario
 * @returns Código de idioma ('es', 'en')
 */
export function getUserLanguage(): SupportedLanguage {
  // En el cliente, verificar primero si hay una cookie de idioma
  if (typeof document !== 'undefined') {
    // Obtener idioma de la cookie
    const cookies = document.cookie.split(';');
    const langCookie = cookies.find(c => c.trim().startsWith('userLanguage='));
    if (langCookie) {
      const langValue = langCookie.split('=')[1];
      if (langValue === 'es' || langValue === 'en') {
        return langValue;
      }
    }
    
    // Si no hay cookie, intentar obtener del navegador
    const navLang = navigator.language.split('-')[0];
    return navLang === 'es' ? 'es' : 'en';
  }
  
  // Por defecto en el servidor
  return 'es';
}

/**
 * Establece el idioma preferido del usuario
 * @param lang Código de idioma ('es', 'en')
 */
export function setUserLanguage(lang: SupportedLanguage): void {
  if (typeof document !== 'undefined') {
    // Establecer cookie con path y dominio adecuados para asegurar accesibilidad
    document.cookie = `userLanguage=${lang};path=/;max-age=${60 * 60 * 24 * 30}`; // 30 días
    
    // Disparar evento personalizado para notificar el cambio de idioma
    const event = new Event('languageChange');
    window.dispatchEvent(event);
    
    // Emitir evento de almacenamiento para que otros componentes se actualicen
    // Este evento es detectado por el LanguageContext
    window.dispatchEvent(new StorageEvent('storage', {
      key: 'userLanguage',
      newValue: lang
    }));
    
    console.log(`[i18n] Idioma cambiado a: ${lang}`);
  }
} 